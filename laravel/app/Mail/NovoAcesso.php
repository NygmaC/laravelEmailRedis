<?php

namespace App\Mail;

use Illuminate\Bus\Queueable;
use Illuminate\Mail\Mailable;
use Illuminate\Queue\SerializesModels;
use Illuminate\Contracts\Queue\ShouldQueue;
use App\User;

class NovoAcesso extends Mailable
{
    use Queueable, SerializesModels;
    protected $user;
    /**
     * Create a new message instance.
     *
     * @return void
     */
    public function __construct(User $user)
    {
        //
        $this->user = $user;
    }

    /**
     * Build the message.
     *
     * @return $this
     */
    public function build()
    {
        //Attach e para enviar anexos
        //primeiro parametro Ã© a path exemplo: public_path() ou base_path()
        // e concatena com a pasta e o nome do arquivo 
        return $this->view('emails.novoacesso')->attach();
    }
}
